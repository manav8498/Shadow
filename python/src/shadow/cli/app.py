@app.command("diagnose-pr", rich_help_panel="Common")
def diagnose_pr_cmd(
    traces: list[Path] = typer.Option(  # noqa: B008
        ...,
        "--traces",
        help="Baseline production-like .agentlog files (or directories).",
    ),
    candidate_traces: list[Path] | None = typer.Option(  # noqa: B008
        None,
        "--candidate-traces",
        help=(
            "Candidate-side .agentlog files paired by filename to baseline. "
            "Omit to skip per-trace diff (Week 1 skeleton path)."
        ),
    ),
    baseline_config: Path = typer.Option(  # noqa: B008
        ...,
        "--baseline-config",
        help="Baseline agent config YAML (same schema as `shadow replay`).",
    ),
    candidate_config: Path = typer.Option(  # noqa: B008
        ...,
        "--candidate-config",
        help="Candidate agent config YAML.",
    ),
    policy: Path | None = typer.Option(  # noqa: B008
        None,
        "--policy",
        help="Optional behavior-policy YAML (shadow.hierarchical schema).",
    ),
    out: Path = typer.Option(  # noqa: B008
        ...,
        "--out",
        help="Path to write the JSON report (diagnose-pr/v0.1 schema).",
    ),
    pr_comment: Path | None = typer.Option(  # noqa: B008
        None,
        "--pr-comment",
        help="Path to write the markdown PR comment (omit to skip).",
    ),
    changed_files: list[str] | None = typer.Option(  # noqa: B008
        None,
        "--changed-files",
        help=(
            "Files changed in the PR (e.g. from `git diff --name-only`); used to "
            "attach human-readable filenames to prompt deltas."
        ),
    ),
    max_traces: int = typer.Option(
        200,
        "--max-traces",
        help="Cap on traces fed to the per-trace loop. Mining selects representatives above this.",
    ),
    fail_on: str = typer.Option(
        "none",
        "--fail-on",
        help="Exit non-zero on this verdict floor: none|probe|hold|stop.",
    ),
    backend: str = typer.Option(
        "recorded",
        "--backend",
        help=(
            "Cause attribution backend: recorded (no backend, simple attribution from observed "
            "deltas) | mock (deterministic per-delta intervention for tests/demos)."
        ),
    ),
    n_bootstrap: int = typer.Option(
        500,
        "--n-bootstrap",
        help="Bootstrap resample count for causal CI. Used only when --backend != recorded.",
    ),
    max_cost: float | None = typer.Option(
        None,
        "--max-cost",
        help=(
            "Maximum total USD spend allowed during --backend live runs. "
            "Aborts before exceeding the cap; ignored for recorded/mock backends."
        ),
    ),
    baseline_ref: str | None = typer.Option(
        None,
        "--baseline-ref",
        help=(
            "Optional git ref (e.g. `origin/main` or a PR base SHA). When set "
            "along with --changed-files, prompt deltas are augmented with "
            "file:line attribution from `git diff <ref>...HEAD`."
        ),
    ),
) -> None:
    """Diagnose a candidate config against production-like traces.

    Composes existing Shadow internals (parser, mining, the 9-axis
    differ, policy checker, causal attribution) into one PR-time
    command surface. Produces a JSON report and a markdown PR
    comment.

    Backends:
      recorded (default): simple attribution from observed deltas
                          (no CI; one-delta runs still get conf=1.0).
      mock              : deterministic per-delta intervention for
                          tests / demos; emits real bootstrap CIs
                          but synthetic ATE magnitudes (the PR
                          comment surfaces a 'synthetic' disclosure).
      live              : real OpenAI replay per baseline trace;
                          divergence is the corpus mean. --max-cost
                          USD caps total spend.
    """
    from shadow.diagnose_pr.render import render_pr_comment
    from shadow.diagnose_pr.report import to_json
    from shadow.diagnose_pr.runner import DiagnoseOptions, run_diagnose_pr

    if fail_on not in {"none", "probe", "hold", "stop"}:
        _fail(Exception(f"--fail-on must be one of [none, probe, hold, stop], got {fail_on!r}"))
        return

    try:
        result = run_diagnose_pr(
            DiagnoseOptions(
                traces=list(traces),
                candidate_traces=list(candidate_traces) if candidate_traces else None,
                baseline_config=baseline_config,
                candidate_config=candidate_config,
                policy=policy,
                changed_files=list(changed_files) if changed_files else None,
                max_traces=max_traces,
                backend=backend,
                n_bootstrap=n_bootstrap,
                max_cost_usd=max_cost,
                baseline_ref=baseline_ref,
            )
        )
    except Exception as exc:
        _fail(exc)
        return

    report = result.report
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(to_json(report) + "\n", encoding="utf-8")
    if pr_comment is not None:
        pr_comment.parent.mkdir(parents=True, exist_ok=True)
        pr_comment.write_text(render_pr_comment(report), encoding="utf-8")

    summary = (
        f"Shadow verdict: {report.verdict.upper()} - "
        f"{report.affected_traces}/{report.total_traces} affected"
    )
    if result.cost_usd is not None:
        summary += f" (live cost: ${result.cost_usd:.4f}, {len(result.cost_breakdown)} API calls)"
    typer.echo(summary)

    rank = {"ship": 0, "probe": 1, "hold": 2, "stop": 3}
    floor_map = {"none": -1, "probe": 1, "hold": 2, "stop": 3}
    verdict_rank = rank.get(report.verdict, 0)
    floor = floor_map[fail_on]
    if floor >= 0 and verdict_rank >= floor:
        raise typer.Exit(code=1)


@app.command("gate-pr", rich_help_panel="Common")
def gate_pr_cmd(
    traces: list[Path] = typer.Option(  # noqa: B008
        ...,
        "--traces",
        help="Baseline production-like .agentlog files.",
    ),
    candidate_traces: list[Path] | None = typer.Option(  # noqa: B008
        None,
        "--candidate-traces",
        help="Candidate .agentlog files paired by filename.",
    ),
    baseline_config: Path = typer.Option(  # noqa: B008
        ...,
        "--baseline-config",
        help="Baseline agent config YAML.",
    ),
    candidate_config: Path = typer.Option(  # noqa: B008
        ...,
        "--candidate-config",
        help="Candidate agent config YAML.",
    ),
    policy: Path | None = typer.Option(  # noqa: B008
        None,
        "--policy",
        help="Optional behavior-policy YAML.",
    ),
    pr_comment: Path | None = typer.Option(  # noqa: B008
        None,
        "--pr-comment",
        help="Path to write the markdown PR comment (CI typically posts this).",
    ),
    out: Path | None = typer.Option(  # noqa: B008
        None,
        "--out",
        help="Optional path to write the JSON report (omit to write tempfile).",
    ),
    changed_files: list[str] | None = typer.Option(  # noqa: B008
        None,
        "--changed-files",
        help="Files changed in the PR (used to attach filenames to deltas).",
    ),
    backend: str = typer.Option(
        "recorded",
        "--backend",
        help="Cause attribution backend: recorded | mock | live.",
    ),
    n_bootstrap: int = typer.Option(
        500,
        "--n-bootstrap",
        help="Bootstrap resample count for causal CI (used when --backend != recorded).",
    ),
    max_traces: int = typer.Option(
        200,
        "--max-traces",
        help="Cap on traces (mining selects representatives above this).",
    ),
    max_cost: float | None = typer.Option(
        None,
        "--max-cost",
        help="Maximum total USD spend for --backend live runs.",
    ),
    baseline_ref: str | None = typer.Option(
        None,
        "--baseline-ref",
        help=(
            "Optional git ref (e.g. `origin/main`). When set with --changed-files, "
            "prompt deltas carry file:line + removed text via `git diff <ref>...HEAD`."
        ),
    ),
    verbose: Annotated[
        bool,
        typer.Option(
            "--verbose",
            "-v",
            help=(
                "Print the full diagnose-pr report to stdout in addition to the "
                "pytest-style 1-screen failure summary. Default is the summary only."
            ),
        ),
    ] = False,
) -> None:
    """CI-friendly wrapper around `shadow diagnose-pr` with verdict-mapped exit codes:

      0 = ship    (no behavior regression)
      1 = probe / hold  (regression detected; merge held)
      2 = stop    (critical violation; do not merge)
      3 = internal/tooling error  (treat as failure in CI)

    On non-ship verdicts, prints a pytest-style 1-screen summary —
    verdict, axis, ATE/CI, dominant cause file:line, and the
    suggested-fix command. Pass `--verbose` for the full JSON
    report on stdout. The full markdown PR comment is always
    written via `--pr-comment`.
    """
    import tempfile

    from shadow.diagnose_pr.render import render_pr_comment
    from shadow.diagnose_pr.report import to_json
    from shadow.diagnose_pr.runner import DiagnoseOptions, run_diagnose_pr

    if out is None:
        out = Path(tempfile.mkdtemp(prefix="shadow-gate-pr-")) / "report.json"

    try:
        result = run_diagnose_pr(
            DiagnoseOptions(
                traces=list(traces),
                candidate_traces=list(candidate_traces) if candidate_traces else None,
                baseline_config=baseline_config,
                candidate_config=candidate_config,
                policy=policy,
                changed_files=list(changed_files) if changed_files else None,
                max_traces=max_traces,
                backend=backend,
                n_bootstrap=n_bootstrap,
                max_cost_usd=max_cost,
                baseline_ref=baseline_ref,
            )
        )
    except Exception as exc:
        # gate-pr never propagates a tooling error to the CI as a clean failure;
        # exit 3 means "investigate" not "merge held".
        err_console.print(f"[bold red]gate-pr error[/]: {exc}")
        raise typer.Exit(code=3) from exc

    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(to_json(result.report) + "\n", encoding="utf-8")
    if pr_comment is not None:
        pr_comment.parent.mkdir(parents=True, exist_ok=True)
        pr_comment.write_text(render_pr_comment(result.report), encoding="utf-8")

    verdict = result.report.verdict
    code = {"ship": 0, "probe": 1, "hold": 1, "stop": 2}.get(verdict, 3)
    if code == 0:
        msg = "[green]✓[/] Shadow gate-pr: SHIP (exit 0)"
        if result.cost_usd is not None:
            msg += f" — live cost: ${result.cost_usd:.4f}"
        err_console.print(msg)
        return

    # Pytest-style 1-screen failure summary. The PR comment carries
    # the full markdown; the local runner gets the five key lines
    # that tell a developer what to do next.
    _render_gate_pr_failure_summary(result.report, cost_usd=result.cost_usd)
    if verbose:
        typer.echo("")
        typer.echo("--- full report (--verbose) ---")
        typer.echo(to_json(result.report))
    raise typer.Exit(code=code)


def _render_gate_pr_failure_summary(
    report: Any,
    *,
    cost_usd: float | None,
) -> None:
    """Print a five-line failure summary in the pytest-assertion shape:
    verdict, axis, ATE/CI, dominant cause `file:line`, and the
    `shadow verify-fix` command. Each line stands alone; together
    they fit on a laptop terminal without scrolling.

    The information already lives on `DiagnosePrReport`; this just
    chooses what to surface front-and-center vs. push behind
    `--verbose` / `--pr-comment` / the JSON report.
    """
    verdict = report.verdict
    color = {"ship": "green", "probe": "yellow", "hold": "yellow", "stop": "red"}.get(
        verdict, "red"
    )
    code = {"ship": 0, "probe": 1, "hold": 1, "stop": 2}.get(verdict, 3)

    # Line 1: verdict + exit code + blast radius.
    affected = report.affected_traces
    total = report.total_traces
    head = (
        f"[bold {color}]✗ Shadow gate-pr: {verdict.upper()}[/] (exit {code}) "
        f"— {affected}/{total} traces affected"
    )
    if cost_usd is not None:
        head += f" — live cost ${cost_usd:.4f}"
    err_console.print(head)

    # Line 2: dominant cause / likely candidate, citing file:line if blame found one.
    dom = report.dominant_cause
    if dom is not None:
        cite = (
            f"{dom.file_path}:{dom.line_no}"
            if dom.file_path is not None and dom.line_no is not None
            else dom.delta_id
        )
        confident = dom.confidence >= 1.0
        verb = "appears to be the main cause" if confident else "is the most likely candidate"
        err_console.print(f"  [bold]Cause[/]: [cyan]{cite}[/] {verb}.")
        if dom.removed_text:
            snippet = dom.removed_text.strip()
            if len(snippet) > 100:
                snippet = snippet[:97] + "..."
            err_console.print(f"  [bold red]- removed[/]: {snippet}")
        # Line 3: numeric evidence.
        ate_str = f"{dom.ate:+.2f}"
        ci_str = (
            f"[{dom.ci_low:.2f}, {dom.ci_high:.2f}]"
            if dom.ci_low is not None and dom.ci_high is not None
            else "n/a"
        )
        e_str = f"{dom.e_value:.1f}" if dom.e_value is not None else "n/a"
        err_console.print(f"  [bold]{dom.axis}[/]: ATE={ate_str}  95% CI={ci_str}  E={e_str}")
    elif report.top_causes:
        names = [c.delta_id for c in sorted(report.top_causes, key=lambda c: c.delta_id)[:3]]
        err_console.print(
            f"  [bold]Likely candidates[/]: {', '.join(names)} — re-run with `--backend live` "
            "for ATE/CI."
        )
    else:
        err_console.print("  [dim]no dominant cause inferred[/]")

    # Line 4: policy violation if any.
    if report.worst_policy_rule and report.new_policy_violations > 0:
        err_console.print(
            f"  [bold]Policy[/]: {report.new_policy_violations} new violation(s) of "
            f"[yellow]{report.worst_policy_rule}[/]"
        )

    # Line 5: actionable next-step command.
    if report.suggested_fix:
        err_console.print(f"  [bold]Fix[/]: {report.suggested_fix}")
    err_console.print(
        "  [dim]Verify:[/] [cyan]shadow verify-fix --report .shadow/diagnose-pr/report.json[/]"
    )


@app.command("dashboard", rich_help_panel="Common")
def dashboard_cmd(
    report: Path = typer.Option(  # noqa: B008
        ...,
        "--report",
        help="Path to a diagnose-pr report.json (or any JSON matching diagnose-pr/v0.1).",
    ),
    port: int = typer.Option(8080, "--port", help="Port to bind. Default 8080."),
    host: str = typer.Option(
        "127.0.0.1",
        "--host",
        help="Bind address. Default localhost; pass 0.0.0.0 to expose on the network.",
    ),
    open_browser: bool = typer.Option(
        False,
        "--open",
        help="Open the dashboard URL in the default browser on startup.",
    ),
) -> None:
    """Serve a diagnose-pr report.json as a browsable web page.

    Single-process, single-report, no auth. Meant for local
    review of CI artefacts or sharing with someone who can't
    run the CLI. Don't expose to the public internet without a
    reverse proxy doing TLS + auth.
    """
    try:
        # uvicorn is only present when the [serve] extra is installed.
        # Suppress import-not-found in the default-extras CI venv
        # (where uvicorn is missing) AND unused-ignore in the
        # all-extras venv (where it has type stubs).
        import uvicorn  # type: ignore[import-not-found, unused-ignore]

        from shadow.diagnose_pr.dashboard import build_app
    except ImportError as exc:
        _fail(
            Exception(
                "shadow dashboard requires fastapi + uvicorn. "
                "Install: pip install 'shadow-diff[serve]' "
                f"({exc})"
            )
        )
        return

    if not report.is_file():
        _fail(Exception(f"report file not found: {report}"))
        return

    app_instance = build_app(report)
    url = f"http://{host}:{port}/"
    typer.echo(f"Shadow dashboard serving {report} at {url}")
    if open_browser:
        import webbrowser

        webbrowser.open(url)
    uvicorn.run(app_instance, host=host, port=port, log_level="warning")


@app.command("verify-fix", rich_help_panel="Common")
def verify_fix_cmd(
    report: Path = typer.Option(  # noqa: B008
        ...,
        "--report",
        help="Path to the diagnose-pr report.json from the previous run.",
    ),
    baseline_traces: list[Path] = typer.Option(  # noqa: B008
        ...,
        "--traces",
        help=(
            "Original baseline .agentlog files (or directories) — "
            "same as `shadow diagnose-pr --traces`."
        ),
    ),
    fixed_traces: list[Path] = typer.Option(  # noqa: B008
        ...,
        "--fixed-traces",
        help="Candidate-with-patch .agentlog files; paired by filename to baseline.",
    ),
    policy: Path | None = typer.Option(  # noqa: B008
        None,
        "--policy",
        help="Optional behavior-policy YAML overlay (same schema as diagnose-pr).",
    ),
    out: Path = typer.Option(  # noqa: B008
        ...,
        "--out",
        help="Path to write the verify-fix JSON report.",
    ),
    affected_threshold: float = typer.Option(
        0.90,
        "--affected-threshold",
        help="Minimum fraction of affected traces that must be reversed (default 0.90).",
    ),
    safe_ceiling: float = typer.Option(
        0.02,
        "--safe-ceiling",
        help="Maximum fraction of previously-safe traces that may regress (default 0.02).",
    ),
) -> None:
    """Verify that a fix reverses the regression named by an
    earlier `shadow diagnose-pr` run, without introducing new ones.

    Reads the diagnose report's `affected_trace_ids`, re-diffs only
    those traces against the candidate-with-patch, and reports the
    pass/fail with explicit fail-reasons. Exit code 1 on fail, 0 on
    pass.
    """
    from dataclasses import asdict

    from shadow.diagnose_pr.loaders import load_traces
    from shadow.diagnose_pr.verify_fix import verify_fix as _verify_fix

    try:
        diagnose = json.loads(report.read_text(encoding="utf-8"))
        base = load_traces(list(baseline_traces))
        fixed = load_traces(list(fixed_traces))
    except Exception as exc:
        _fail(exc)
        return

    try:
        result = _verify_fix(
            diagnose_report=diagnose,
            baseline_traces=base,
            fixed_traces=fixed,
            policy_path=policy,
            affected_reversed_threshold=affected_threshold,
            safe_regression_ceiling=safe_ceiling,
        )
    except Exception as exc:
        _fail(exc)
        return

    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(json.dumps(asdict(result), sort_keys=True, indent=2) + "\n", encoding="utf-8")

    if result.passed:
        typer.echo(
            f"Shadow verify-fix: PASS — "
            f"{result.affected_reversed}/{result.affected_total} affected reversed, "
            f"{result.safe_regressed}/{result.safe_total} safe regressed"
        )
    else:
        typer.echo(f"Shadow verify-fix: FAIL — {'; '.join(result.fail_reasons)}")
        raise typer.Exit(code=1)


# ---- helpers ---------------------------------------------------------------


def _fail(e: BaseException) -> None:
    err_console.print(f"[bold red]error[/]: {e}")
    # Hint for the most common first-contact failures. The original
    # message is unchanged (log-scrapers keep working); we just append
    # one extra line pointing at recovery for two specific error classes.
    if isinstance(e, FileNotFoundError):
        err_console.print(
            "[dim]hint: check the path. New to Shadow? `shadow demo` "
            "runs a real diff against bundled fixtures with no setup.[/]"
        )
    elif isinstance(e, PermissionError):
        err_console.print(
            "[dim]hint: file exists but isn't readable. Check ownership "
            "and permissions with `ls -l <path>`.[/]"
        )
    raise typer.Exit(code=1)


def _force_utf8_io() -> None:
    """Force stdout/stderr to UTF-8 so Rich's box-drawing + `⚠`/`✓`/`✗`
    glyphs don't crash on Windows' cp1252 default.

    PEP 597 suggests this for Python applications that emit non-ASCII
    by design. No-op on POSIX (already UTF-8 in >99% of real-world
    setups) and on Python 3.15+ where UTF-8 mode is the default.
    """
    for stream in (sys.stdout, sys.stderr):
        try:
            if hasattr(stream, "reconfigure"):
                stream.reconfigure(encoding="utf-8", errors="replace")
        except Exception:
            # Swallow: the CLI should still start even if we can't
            # rebind the stream (e.g. if it's been wrapped by a test
            # harness). Rich has its own fallback for un-encodable
            # characters.
            pass


def main() -> None:
    """Entry point registered as the `shadow` console_script."""
    _force_utf8_io()
    app()


if __name__ == "__main__":
    main()
