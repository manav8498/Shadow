GITHUB_ACTION_DIAGNOSE_PR_WORKFLOW = """\
# .github/workflows/shadow-diagnose-pr.yml — generated by
# `shadow init --github-action`
#
# Runs `shadow diagnose-pr` on every pull request: names the exact
# config change that broke the agent (or "likely candidates" when
# multiple changes plausibly explain the regression), posts the
# verdict + suggested fix as a PR comment, and gates the merge with
# verdict-mapped exit codes via `shadow gate-pr`:
#
#   ship   -> exit 0 (clear to merge)
#   probe  -> exit 1 (uncertain — investigate)
#   hold   -> exit 1 (regression detected — held)
#   stop   -> exit 2 (critical policy violation — must not merge)
#   error  -> exit 3 (treated as failure)
#
# Edit the BASELINE_TRACES / CANDIDATE_TRACES / *_CONFIG paths to
# point at the .agentlog directories and YAML configs you commit
# in your repo. Switch BACKEND to `live` (and add an OPENAI_API_KEY
# secret) when you want real intervention-based ATE + CI + E-value.
name: shadow diagnose-pr

on:
  pull_request:
    paths:
      # Re-run whenever an agent config or committed trace changes.
      # Add globs for your own layout as needed.
      - "configs/**"
      - "fixtures/**.agentlog"
      - "prompts/**"
      - ".github/workflows/shadow-diagnose-pr.yml"

permissions:
  pull-requests: write  # required to post the diagnose-pr comment
  contents: read

jobs:
  diagnose:
    name: shadow diagnose-pr
    runs-on: ubuntu-latest
    env:
      # Edit these paths to point at your committed trace + config layout.
      BASELINE_TRACES: fixtures/baseline_traces
      CANDIDATE_TRACES: fixtures/candidate_traces
      BASELINE_CONFIG: configs/baseline.yaml
      CANDIDATE_CONFIG: configs/candidate.yaml
      POLICY: configs/shadow-policy.yaml
      # `recorded` (default): offline, free, no causal CI.
      # `mock`: deterministic synthetic per-delta intervention (CI, E-value).
      # `live`: real OpenAI replay per baseline trace (set MAX_COST_USD).
      BACKEND: recorded
      MAX_COST_USD: "5.00"
    steps:
      - uses: actions/checkout@v4
        with:
          fetch-depth: 0  # diagnose-pr can blame prompt diffs against base

      - uses: actions/setup-python@v5
        with:
          python-version: "3.11"

      - name: Install shadow-diff
        # Pinned to the same major as the Shadow that scaffolded this
        # workflow. Re-run `shadow init --github-action --force` after
        # upgrading past a major to refresh the constraint.
        run: pip install --upgrade "shadow-diff>={MAJOR},<{NEXT_MAJOR}"

      - name: Collect changed files
        id: changed
        run: |
          BASE_SHA="${{ github.event.pull_request.base.sha }}"
          # Newline-separated list; the action reads these so the PR
          # comment can cite filenames (e.g. `prompts/refund.md`)
          # rather than just the flattened config path.
          git diff --name-only "$BASE_SHA"...HEAD > changed.txt || true
          echo "list<<EOF" >> "$GITHUB_OUTPUT"
          cat changed.txt >> "$GITHUB_OUTPUT"
          echo "EOF" >> "$GITHUB_OUTPUT"

      - name: Run shadow diagnose-pr (gate-pr maps verdict to exit code)
        env:
          # Only set this in the workflow secrets when BACKEND=live.
          OPENAI_API_KEY: ${{ secrets.OPENAI_API_KEY }}
          GH_TOKEN: ${{ github.token }}
          BASE_SHA: ${{ github.event.pull_request.base.sha }}
        run: |
          mkdir -p .shadow/diagnose-pr
          # Build the argv list. --changed-files is repeated once per
          # changed file so the runner can use them for prompt-blame
          # attribution; --baseline-ref enables hunk-level
          # `prompts/foo.md:LINE removed: "..."` citation in the PR
          # comment when a prompt file changed.
          ARGS=(
            --traces "$BASELINE_TRACES"
            --candidate-traces "$CANDIDATE_TRACES"
            --baseline-config "$BASELINE_CONFIG"
            --candidate-config "$CANDIDATE_CONFIG"
            --policy "$POLICY"
            --backend "$BACKEND"
            --max-cost "$MAX_COST_USD"
            --baseline-ref "$BASE_SHA"
            --out .shadow/diagnose-pr/report.json
            --pr-comment .shadow/diagnose-pr/comment.md
          )
          if [ -s changed.txt ]; then
            while IFS= read -r f; do
              [ -z "$f" ] && continue
              ARGS+=( --changed-files "$f" )
            done < changed.txt
          fi
          shadow gate-pr "${ARGS[@]}"

      - name: Post PR comment
        if: always()  # post the comment even when gate-pr exits non-zero
        env:
          GH_TOKEN: ${{ github.token }}
        run: |
          if [ -f .shadow/diagnose-pr/comment.md ]; then
            gh pr comment "${{ github.event.pull_request.number }}" \\
              --body-file .shadow/diagnose-pr/comment.md
          fi
"""


GITHUB_ACTION_LEGACY_DIFF_WORKFLOW = """\
# .github/workflows/shadow-diff.yml — generated by
# `shadow init --github-action --legacy-diff`
#
# Legacy raw nine-axis-diff workflow (no causal attribution).
# Most repos should use `shadow init --github-action` (without
# --legacy-diff) which scaffolds the diagnose-pr flow.
name: shadow diff

on:
  pull_request:
    paths:
      # Re-run whenever an agent config or committed trace changes.
      # Add globs for your own layout as needed.
      - "configs/**"
      - "fixtures/**.agentlog"
      - ".github/workflows/shadow-diff.yml"

permissions:
  pull-requests: write  # required to post the diff comment
  contents: read

jobs:
  diff:
    name: shadow behavioural diff
    runs-on: ubuntu-latest
    env:
      # Edit these two paths to point at your committed fixtures.
      BASELINE: fixtures/baseline.agentlog
      CANDIDATE: fixtures/candidate.agentlog
    steps:
      - uses: actions/checkout@v4

      - uses: actions/setup-python@v5
        with:
          python-version: "3.11"

      - name: Install shadow-diff
        # Pinned to the same major as the Shadow that scaffolded this
        # workflow. Re-run `shadow init --github-action --force` after
        # upgrading past a major to refresh the constraint.
        run: pip install --upgrade "shadow-diff>={MAJOR},<{NEXT_MAJOR}"

      - name: Compute diff
        run: |
          shadow diff "$BASELINE" "$CANDIDATE" \\
            --output-json diff.json \\
            || true  # diff can exit non-zero on severe regression; we still want the comment

      - name: Render PR comment
        run: shadow report diff.json --format github-pr > comment.md

      - name: Post comment on PR
        env:
          GH_TOKEN: ${{ github.token }}
        run: |
          gh pr comment "${{ github.event.pull_request.number }}" --body-file comment.md

      # OPTIONAL — gate the merge on regressions.
      # Uncomment this step (and add a `--policy <file>` flag if you
      # have one) to fail the check when the worst axis severity or
      # policy regression hits the threshold. The PR comment is already
      # posted above, so a blocked PR still has the explanation.
      #
      # - name: Block merge on severe regression
      #   run: shadow diff "$BASELINE" "$CANDIDATE" --fail-on severe
"""


def _detect_project_kind(root: Path) -> str:
    """Best-effort detection of the host project's primary language.

    Walks `root` and the immediate parent looking for sentinel files.
    Returns one of `python` / `node` / `rust` / `unknown`. Used by
    `shadow init` only to surface a one-line confirmation in the
    next-step output — Shadow doesn't *do* anything different per
    project type. The value mirrors what the user already knows
    about their repo, which converts "Shadow knows my stack" into
    a small trust signal in the activation moment.
    """
    sentinels = {
        "pyproject.toml": "python",
        "setup.py": "python",
        "package.json": "node",
        "Cargo.toml": "rust",
    }
    for r in (root, root.parent):
        for name, kind in sentinels.items():
            if (r / name).is_file():
                return kind
    return "unknown"


@app.command(rich_help_panel="Setup")
def init(
    path: Annotated[Path, typer.Argument(help="Project root (defaults to cwd)")] = Path("."),
    github_action: Annotated[
        bool,
        typer.Option(
            "--github-action",
            help="Also scaffold a GitHub Actions workflow. Defaults to "
            "`.github/workflows/shadow-diagnose-pr.yml` (the diagnose-pr "
            "wedge). Pass `--legacy-diff` for the older raw nine-axis "
            "`shadow diff` workflow.",
        ),
    ] = False,
    legacy_diff: Annotated[
        bool,
        typer.Option(
            "--legacy-diff",
            help="With --github-action, scaffold the legacy raw "
            "`shadow diff` workflow instead of the diagnose-pr flow. "
            "Most repos should not pass this flag.",
        ),
    ] = False,
) -> None:
    """Scaffold a Shadow project: `.shadow/` dirs, `shadow.yaml`, and (optionally) CI.

    Detects whether this is a Python / Node / Rust project from the
    sentinel files in cwd or its parent, drops a starter
    `shadow.yaml` (the conventional config Shadow looks for), and
    ends with a copy-pasteable next-step command.

    Pass `--github-action` to also drop a ready-to-commit GitHub
    Actions workflow:

      * default — `.github/workflows/shadow-diagnose-pr.yml` runs
        `shadow diagnose-pr` (causal regression forensics) on every
        PR, posts a verdict + suggested fix as a comment, and gates
        the merge with verdict-mapped exit codes.
      * with `--legacy-diff` — `.github/workflows/shadow-diff.yml`
        runs the older raw nine-axis `shadow diff`. Kept for repos
        that prefer the older flow.

    Run `shadow quickstart` (in any directory) to scaffold a fully
    runnable demo with baseline + candidate fixtures you can gate
    against immediately.
    """
    from shadow.baseline import (
        SHADOW_YAML_NAME,
        ShadowConfig,
        load_shadow_yaml,
        save_shadow_yaml,
    )

    try:
        shadow_dir = path / ".shadow"
        (shadow_dir / "traces").mkdir(parents=True, exist_ok=True)
        (shadow_dir / "replays").mkdir(parents=True, exist_ok=True)
        (shadow_dir / "cache").mkdir(parents=True, exist_ok=True)
        config_path = shadow_dir / "config.toml"
        if not config_path.exists():
            config_path.write_text(DEFAULT_CONFIG_TOML)

        # Top-level `shadow.yaml`. This is the file `shadow gate-pr`,
        # `shadow baseline verify`, etc. read for project defaults.
        # `shadow init` writes a minimal stub so users can see the
        # conventional shape; `shadow baseline create` later fills in
        # the baseline pin.
        shadow_yaml_path = path / SHADOW_YAML_NAME
        existed_before = shadow_yaml_path.is_file()
        cfg = load_shadow_yaml(shadow_yaml_path) if existed_before else ShadowConfig()
        save_shadow_yaml(shadow_yaml_path, cfg)

        kind = _detect_project_kind(path.resolve())
        err_console.print(f"[green]✓[/] initialised shadow store at {shadow_dir}")
        if existed_before:
            err_console.print(f"[yellow]·[/] {shadow_yaml_path} already existed — left alone.")
        else:
            err_console.print(f"[green]✓[/] wrote {shadow_yaml_path} (project: {kind})")

        if github_action:
            workflow_dir = path / ".github" / "workflows"
            workflow_dir.mkdir(parents=True, exist_ok=True)
            if legacy_diff:
                workflow_filename = "shadow-diff.yml"
                template = GITHUB_ACTION_LEGACY_DIFF_WORKFLOW
                next_step_hint = (
                    "    edit BASELINE / CANDIDATE paths, commit, and "
                    "every PR will get a diff comment."
                )
            else:
                workflow_filename = "shadow-diagnose-pr.yml"
                template = GITHUB_ACTION_DIAGNOSE_PR_WORKFLOW
                next_step_hint = (
                    "    edit BASELINE_TRACES / CANDIDATE_TRACES / "
                    "*_CONFIG / POLICY paths, commit, and every PR "
                    "will get a diagnose-pr comment with the dominant "
                    "cause + suggested fix.\n"
                    "    Switch BACKEND=live (and add an OPENAI_API_KEY "
                    "secret) for real intervention-based ATE + CI."
                )
            workflow_path = workflow_dir / workflow_filename
            if workflow_path.exists():
                err_console.print(f"[yellow]·[/] {workflow_path} already exists — not overwritten.")
            else:
                # Substitute the pip-install version constraint to track
                # whichever Shadow major is scaffolding the workflow.
                # Using simple .replace() rather than .format() so the
                # GitHub Actions ${{ ... }} syntax inside the template
                # passes through untouched.
                major = int(__version__.split(".", 1)[0])
                content = template.replace("{MAJOR}", str(major)).replace(
                    "{NEXT_MAJOR}", str(major + 1)
                )
                workflow_path.write_text(content)
                err_console.print(f"[green]✓[/] wrote {workflow_path}\n{next_step_hint}")

        # Actionable closer: the user just ran `shadow init`. Tell
        # them the exact command(s) to run next so they see the
        # tool work without reading docs.
        err_console.print("")
        err_console.print("[bold]Next:[/]")
        err_console.print(
            "  1. [cyan]shadow quickstart[/]              "
            "# scaffolds a runnable baseline+candidate demo"
        )
        err_console.print(
            "  2. [cyan]cd shadow-quickstart && shadow demo[/]   "
            "# see Shadow catch a synthetic regression"
        )
        err_console.print(
            "  3. [cyan]shadow baseline create <traces-dir>[/]   "
            "# pin your real agent's baseline"
        )
        err_console.print(
            "  4. [cyan]shadow gate-pr ...[/]                 " "# gate every PR against the pin"
        )
    except Exception as e:
        _fail(e)


