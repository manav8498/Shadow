@app.command(
    # Without these, Typer 0.24 treats the `--` separator as "end of
    # options" but still errors out on unrecognised positional args
    # after it. Explicit per-command allow_extra_args forwards the
    # tokens after `--` to ctx.args as raw strings.
    context_settings={"allow_extra_args": True, "ignore_unknown_options": True},
    rich_help_panel="Common",
)
def record(
    ctx: typer.Context,
    output: Annotated[
        Path, typer.Option("--output", "-o", help="Where to write the .agentlog file")
    ] = Path("recording.agentlog"),
    tags: Annotated[
        str | None,
        typer.Option(
            "--tags",
            help="Comma-separated key=value tags for the trace metadata "
            "(e.g. --tags env=dev,branch=main).",
        ),
    ] = None,
    no_auto_instrument: Annotated[
        bool,
        typer.Option(
            "--no-auto-instrument",
            help="Disable zero-config instrumentation. Use this when the "
            "wrapped command already opens its own `Session` and you don't "
            "want a second one wrapping it.",
        ),
    ] = False,
) -> None:
    """Run a subcommand with zero-config auto-instrumentation.

    Examples:
        shadow record -o demo.agentlog -- python agent.py
        shadow record -o demo.agentlog -- node my-agent.js
        shadow record -o demo.agentlog -- npx tsx my-agent.ts
        shadow record -o demo.agentlog -- ts-node my-agent.ts

    For Python commands, the wrapped interpreter loads a
    sitecustomize shim that starts a `Session` around the whole
    process. For Node-family runtimes (node / npx / tsx / ts-node),
    `NODE_OPTIONS='--import shadow-diff/auto'` is injected so the
    TypeScript SDK's auto entrypoint starts a Session on startup
    (requires Node >= 20.6). For Bun (`bun` / `bunx`), the CLI
    inserts `--preload shadow-diff/auto` into argv — the same auto
    entrypoint runs as a Bun preload. Either way, every
    `anthropic.*.messages.create` /
    `openai.*.chat.completions.create` call made by the agent gets
    recorded without touching agent code.

    The Node / Bun auto paths require `shadow-diff` to be installed
    in the wrapped command's working tree (`npm install shadow-diff`,
    `bun add shadow-diff`, or a workspace dependency).

    If the wrapped command already opens its own `Session` on its
    own, pass `--no-auto-instrument` to avoid a double session.
    """
    args = ctx.args
    if not args:
        err_console.print("[red]error[/]: specify a command after `--`")
        raise typer.Exit(code=2)

    # Fail fast if the output path isn't writable. Catching this here —
    # before the agent runs — prevents wasting LLM API calls on a
    # recording that can't be saved.
    try:
        resolved = output.resolve()
        resolved.parent.mkdir(parents=True, exist_ok=True)
        # Probe actual writability (mkdir tolerates read-only parents
        # silently on some FSes; an open-for-append round-trips the real
        # permission check and leaves no partial bytes behind).
        with resolved.open("ab"):
            pass
        if resolved.stat().st_size == 0:
            # We just created the file via append; clean up so an empty
            # .agentlog doesn't mislead `shadow diff` later.
            resolved.unlink()
    except OSError as e:
        err_console.print(
            f"[red]error[/]: output path not writable: {output}\n"
            f"  reason: {e}\n"
            f"  hint: check the directory exists and you have write permission, "
            "then re-run."
        )
        raise typer.Exit(code=2) from None

    env = dict(os.environ)
    env["SHADOW_SESSION_OUTPUT"] = str(resolved)
    if tags:
        env["SHADOW_SESSION_TAGS"] = tags
    if not no_auto_instrument:
        # Prepend the bootstrap dir so our sitecustomize wins over any
        # other sitecustomize on the user's PYTHONPATH. Python's site.py
        # imports the first `sitecustomize` it finds on sys.path, so
        # prepending is the deterministic win.
        from shadow.sdk import _bootstrap

        bootstrap_dir = str(Path(_bootstrap.__file__).resolve().parent)
        existing = env.get("PYTHONPATH", "")
        env["PYTHONPATH"] = f"{bootstrap_dir}{os.pathsep}{existing}" if existing else bootstrap_dir

        # Mirror the same zero-config flow for Node-family runtimes
        # so `shadow record -- node my-agent.js` Just Works against
        # arbitrary OpenAI/Anthropic JS SDK callers, including
        # LangChain.js / LangGraph TS agents. The TS SDK ships an
        # auto-entrypoint at `shadow-diff/auto` that reads the same
        # SHADOW_SESSION_OUTPUT / SHADOW_SESSION_TAGS env we just set
        # and starts a Session with autoInstrument(). We inject it
        # via NODE_OPTIONS so the user doesn't have to add `--import`
        # themselves. Requires Node >=20.6 (--import flag).
        if _is_node_runtime(args[0]):
            shadow_auto = "shadow-diff/auto"
            inject = f"--import {shadow_auto}"
            existing_node_opts = env.get("NODE_OPTIONS", "").strip()
            env["NODE_OPTIONS"] = f"{inject} {existing_node_opts}" if existing_node_opts else inject
        elif _is_bun_runtime(args[0]):
            # Bun has its own preload mechanism — `bun --preload <module>`
            # — that runs the preloaded module BEFORE the user's
            # entrypoint, with `process.argv[1]` already set to the
            # entrypoint. The TS SDK's `shadow-diff/auto` entrypoint
            # uses argv[1] as its resolution anchor, so it works
            # under both `--import` (Node) and `--preload` (Bun).
            #
            # We mutate argv to prepend `--preload shadow-diff/auto`
            # immediately after the bun executable. This is the only
            # in-process way to inject a preload into bun — there's
            # no NODE_OPTIONS-equivalent env variable that bun honours
            # for an arbitrary preload module (BUN_INSPECT_PRELOAD is
            # inspector-specific).
            args = [args[0], "--preload", "shadow-diff/auto", *args[1:]]
    result = subprocess.run(args, env=env, check=False)
    raise typer.Exit(code=result.returncode)


# Node-family runtime executables that respect the `NODE_OPTIONS` env
# var the same way `node` itself does. `shadow record` injects
# `--import shadow-diff/auto` when the wrapped command starts with one
# of these; consumers like LangChain.js / LangGraph TS pick up
# zero-config recording without touching their agent code.
#
_NODE_RUNTIME_BINARIES = frozenset(
    {
        "node",
        "node.exe",
        "nodejs",
        "npx",
        "npx.cmd",
        "tsx",
        "ts-node",
        "ts-node.cmd",
        # Package-manager CLIs spawn Node child processes that
        # inherit NODE_OPTIONS — so injecting it on the parent
        # propagates to whichever interpreter actually runs the
        # user's agent. The TS auto entrypoint detects "I am the
        # package-manager wrapper" via process.argv[1] and skips
        # itself, so only the actual user-agent process records.
        "npm",
        "npm.cmd",
        "pnpm",
        "pnpm.cmd",
        "yarn",
        "yarn.cmd",
    }
)

# Bun runtime binaries. Bun ships its own JS runtime + bundler and is
# used by BrowserOS-style agents; it does NOT honour NODE_OPTIONS but
# DOES support a `--preload <module>` argv flag that runs a module
# before the user's entrypoint. We detect bun separately so the
# `record` command can inject `--preload shadow-diff/auto` into argv
# instead of mutating NODE_OPTIONS.
_BUN_RUNTIME_BINARIES = frozenset(
    {
        "bun",
        "bun.exe",
        "bunx",
        "bunx.exe",
    }
)


def _is_node_runtime(executable: str) -> bool:
    """True when `executable` is a Node-family CLI that honors NODE_OPTIONS.

    Matches against the basename so `/usr/local/bin/node` and the bare
    `node` both register. Treats both `/` and `\\` as separators so a
    Windows path like `C:\\Program Files\\nodejs\\node.exe` is
    classified correctly when this code runs on a non-Windows host
    (used to build the env for a wrapped subprocess; the actual
    runtime check happens by string match, not by os.path semantics).
    """
    basename = _basename(executable)
    if basename in _NODE_RUNTIME_BINARIES:
        return True
    # Some installs symlink with a version suffix (`node18`, `node20`).
    # Conservative match — only `node` followed by digits.
    return basename.startswith("node") and basename[4:].isdigit()


def _is_bun_runtime(executable: str) -> bool:
    """True when ``executable`` is the Bun JS runtime / bunx wrapper.

    Bun doesn't honour ``NODE_OPTIONS``; the caller injects
    ``--preload shadow-diff/auto`` into argv instead. Cross-platform
    basename matching mirrors :func:`_is_node_runtime`.
    """
    return _basename(executable) in _BUN_RUNTIME_BINARIES


def _basename(path: str) -> str:
    """Cross-platform basename — handles both ``/`` and ``\\`` separators."""
    # Manual basename so cross-platform paths classify identically.
    # pathlib.Path on POSIX won't strip `\\` segments, so the bare
    # rsplit handles that. lower() folds the `.exe` / `.cmd` casing.
    last_slash = max(path.rfind("/"), path.rfind("\\"))
    return path[last_slash + 1 :].lower() if last_slash >= 0 else path.lower()


