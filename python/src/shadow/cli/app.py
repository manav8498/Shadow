GITHUB_ACTION_WORKFLOW = """\
# .github/workflows/shadow-diff.yml — generated by `shadow init --github-action`
#
# Runs Shadow's nine-axis behavioural diff on every pull request and
# posts the result as a PR comment. Edit the `BASELINE` / `CANDIDATE`
# paths to point at trace fixtures you commit in your repo.
name: shadow diff

on:
  pull_request:
    paths:
      # Re-run whenever an agent config or committed trace changes.
      # Add globs for your own layout as needed.
      - "configs/**"
      - "fixtures/**.agentlog"
      - ".github/workflows/shadow-diff.yml"

permissions:
  pull-requests: write  # required to post the diff comment
  contents: read

jobs:
  diff:
    name: shadow behavioural diff
    runs-on: ubuntu-latest
    env:
      # Edit these two paths to point at your committed fixtures.
      BASELINE: fixtures/baseline.agentlog
      CANDIDATE: fixtures/candidate.agentlog
    steps:
      - uses: actions/checkout@v4

      - uses: actions/setup-python@v5
        with:
          python-version: "3.11"

      - name: Install shadow-diff
        # Pinned to the same major as the Shadow that scaffolded this
        # workflow. Re-run `shadow init --github-action --force` after
        # upgrading past a major to refresh the constraint.
        run: pip install --upgrade "shadow-diff>={MAJOR},<{NEXT_MAJOR}"

      - name: Compute diff
        run: |
          shadow diff "$BASELINE" "$CANDIDATE" \\
            --output-json diff.json \\
            || true  # diff can exit non-zero on severe regression; we still want the comment

      - name: Render PR comment
        run: shadow report diff.json --format github-pr > comment.md

      - name: Post comment on PR
        env:
          GH_TOKEN: ${{ github.token }}
        run: |
          gh pr comment "${{ github.event.pull_request.number }}" --body-file comment.md

      # OPTIONAL — gate the merge on regressions.
      # Uncomment this step (and add a `--policy <file>` flag if you
      # have one) to fail the check when the worst axis severity or
      # policy regression hits the threshold. The PR comment is already
      # posted above, so a blocked PR still has the explanation.
      #
      # - name: Block merge on severe regression
      #   run: shadow diff "$BASELINE" "$CANDIDATE" --fail-on severe
"""


@app.command(rich_help_panel="Setup")
def init(
    path: Annotated[Path, typer.Argument(help="Project root (defaults to cwd)")] = Path("."),
    github_action: Annotated[
        bool,
        typer.Option(
            "--github-action",
            help="Also scaffold `.github/workflows/shadow-diff.yml` — a pre-wired "
            "GitHub Actions workflow that runs `shadow diff` on every PR and "
            "posts the result as a comment.",
        ),
    ] = False,
) -> None:
    """Scaffold `.shadow/` in PATH. Creates dirs, config.toml, sqlite index.

    Pass `--github-action` to also drop a ready-to-commit GitHub
    Actions workflow under `.github/workflows/shadow-diff.yml`. Edit
    the `BASELINE` / `CANDIDATE` env vars in that file to point at
    your repo's committed trace fixtures, then git-commit and push —
    every PR will get a diff comment.
    """
    try:
        shadow_dir = path / ".shadow"
        (shadow_dir / "traces").mkdir(parents=True, exist_ok=True)
        (shadow_dir / "replays").mkdir(parents=True, exist_ok=True)
        (shadow_dir / "cache").mkdir(parents=True, exist_ok=True)
        config_path = shadow_dir / "config.toml"
        if not config_path.exists():
            config_path.write_text(DEFAULT_CONFIG_TOML)
        err_console.print(f"[green]✓[/] initialised shadow store at {shadow_dir}")

        if github_action:
            workflow_dir = path / ".github" / "workflows"
            workflow_dir.mkdir(parents=True, exist_ok=True)
            workflow_path = workflow_dir / "shadow-diff.yml"
            if workflow_path.exists():
                err_console.print(f"[yellow]·[/] {workflow_path} already exists — not overwritten.")
            else:
                # Substitute the pip-install version constraint to track
                # whichever Shadow major is scaffolding the workflow.
                # Using simple .replace() rather than .format() so the
                # GitHub Actions ${{ ... }} syntax inside the template
                # passes through untouched.
                major = int(__version__.split(".", 1)[0])
                content = GITHUB_ACTION_WORKFLOW.replace("{MAJOR}", str(major)).replace(
                    "{NEXT_MAJOR}", str(major + 1)
                )
                workflow_path.write_text(content)
                err_console.print(
                    f"[green]✓[/] wrote {workflow_path}\n"
                    "    edit BASELINE / CANDIDATE paths, commit, and every PR "
                    "will get a diff comment."
                )
    except Exception as e:
        _fail(e)


