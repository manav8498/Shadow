def gate_cmd(
    report_json: Annotated[
        Path,
        typer.Argument(help="Path to a report.json produced by `shadow diff --output-json`."),
    ],
    fail_on: Annotated[
        str,
        typer.Option(
            "--fail-on",
            help="Exit 1 when the worst signal hits this level: minor / moderate / severe / never.",
        ),
    ] = "severe",
    policy_file: Annotated[
        Path | None,
        typer.Option(
            "--policy",
            help=(
                "Optional policy YAML to also gate on. Requires --baseline and --candidate "
                "since policy regressions are recomputed from the original traces."
            ),
        ),
    ] = None,
    baseline: Annotated[
        Path | None,
        typer.Option("--baseline", help="Baseline trace (required when --policy is set)."),
    ] = None,
    candidate: Annotated[
        Path | None,
        typer.Option("--candidate", help="Candidate trace (required when --policy is set)."),
    ] = None,
) -> None:
    """Apply --fail-on to a saved diff report without re-running the diff.

    Designed for CI flows that already have a report.json from
    `shadow diff --output-json`: avoids the second full diff invocation
    just to check the merge gate. With --policy, also gates on policy
    regressions recomputed from the original traces.
    """
    try:
        report = json.loads(report_json.read_text())
        b: list[dict[str, Any]] | None = None
        c: list[dict[str, Any]] | None = None
        if policy_file is not None:
            if baseline is None or candidate is None:
                err_console.print("[red]error[/]: --policy requires --baseline and --candidate.")
                raise typer.Exit(code=2)
            from shadow import _core

            b = _core.parse_agentlog(baseline.read_bytes())
            c = _core.parse_agentlog(candidate.read_bytes())
        _apply_gate(report, fail_on, baseline=b, candidate=c, policy_file=policy_file)
    except typer.Exit:
        raise
    except ShadowError as e:
        _fail(e)
    except Exception as e:
        _fail(e)


def _resolve_auto_judge(judge: JudgeKind, backend: JudgeBackend) -> tuple[JudgeKind, JudgeBackend]:
    """Expand `--judge auto` into a concrete (judge, backend) pair.

    Preference order is ANTHROPIC_API_KEY (cheaper Haiku 4.5) then
    OPENAI_API_KEY (gpt-4o-mini). If neither is set we quietly fall
    through to `none` — never silently skip for user-selected values.
    """
    if judge is not JudgeKind.auto:
        return judge, backend

    if os.environ.get("ANTHROPIC_API_KEY"):
        err_console.print(
            "[dim]judge: auto -> sanity on anthropic " "(Claude Haiku 4.5, ~$0.0003 per diff).[/]"
        )
        return JudgeKind.sanity, JudgeBackend.anthropic
    if os.environ.get("OPENAI_API_KEY"):
        err_console.print(
            "[dim]judge: auto -> sanity on openai " "(gpt-4o-mini, ~$0.0003 per diff).[/]"
        )
        return JudgeKind.sanity, JudgeBackend.openai
    err_console.print(
        "[dim]judge: auto requested but no ANTHROPIC_API_KEY or OPENAI_API_KEY "
        "found; axis 8 stays empty. Set either env var and re-run for a "
        "one-cent signal.[/]"
    )
    return JudgeKind.none, backend


def _generate_explanation(report: dict[str, Any], *, backend_name: str, model: str | None) -> str:
    """One-paragraph plain-English summary via the judge backend."""
    from shadow.llm import get_backend
    from shadow.report.summary import summarise_report

    determ = summarise_report(report)
    prompt = (
        "You are reviewing a Shadow behavioural diff between a baseline and a "
        "candidate agent trace. The deterministic summary below is the raw "
        "signal. Restate it for a tech lead in one short paragraph: what "
        "regressed, by how much, and the single highest-priority thing to "
        "verify. No pleasantries, no hedging, no preamble. <= 80 words.\n\n"
        f"Deterministic summary:\n{determ}"
    )
    backend = get_backend(backend_name)
    request: dict[str, Any] = {
        "model": model or "",
        "messages": [{"role": "user", "content": prompt}],
        "params": {"temperature": 0.0, "max_tokens": 256},
    }
    try:
        response = asyncio.run(backend.complete(request))
    except Exception as e:
        err_console.print(f"[yellow]warning[/]: --explain call failed: {e}")
        return ""
    content = response.get("content") or []
    if isinstance(content, str):
        return content.strip()
    parts: list[str] = []
    for part in content:
        if isinstance(part, dict) and part.get("type") == "text":
            text = part.get("text")
            if isinstance(text, str):
                parts.append(text)
    return "\n".join(parts).strip()


def load_pricing_file(path: Path) -> dict[str, Any]:
    """Read a pricing JSON file from disk and parse it through the
    metadata-aware loader. Single helper so every consumer (CLI diff,
    CLI mine, CLI certify, MCP server) shares the same parser and
    silent _comment / _updated / _version skip behaviour. Without
    this, the MCP server's shadow_diff handler raw-loaded the same
    pricing.json the CLI accepted and crashed on the _comment key.
    """
    raw = json.loads(path.read_text())
    return _parse_pricing_table(raw)


def _parse_pricing_table(raw: dict[str, Any]) -> dict[str, Any]:
    """Accept either legacy [input, output] tuple form or rich dict form.

    Rich form:
        "claude-opus-4-7": {
            "input": 0.000015,
            "output": 0.000075,
            "cached_input": 0.0000015,
            "reasoning": 0.0,
            "batch_discount": 0.5
        }
    """
    out: dict[str, Any] = {}
    for model, value in raw.items():
        # Skip metadata keys (e.g. "_comment", "_updated", "_version"). The
        # in-tree pricing.json uses underscore-prefixed top-level keys for
        # documentation; without this skip, every user pointing --pricing at
        # the canonical file got "must be [input, output] or a dict".
        if isinstance(model, str) and model.startswith("_"):
            continue
        if isinstance(value, list | tuple) and len(value) == 2:
            out[model] = (float(value[0]), float(value[1]))
        elif isinstance(value, dict):
            out[model] = {
                "input": float(value.get("input", 0.0)),
                "output": float(value.get("output", 0.0)),
                "cached_input": float(value.get("cached_input", 0.0)),
                "cached_write_5m": float(value.get("cached_write_5m", 0.0)),
                "cached_write_1h": float(value.get("cached_write_1h", 0.0)),
                "reasoning": float(value.get("reasoning", 0.0)),
                "batch_discount": float(value.get("batch_discount", 0.0)),
            }
        else:
            got_repr = type(value).__name__
            raise ShadowError(
                f"pricing entry for {model!r} has invalid shape "
                f"(got {got_repr}: {value!r}).\n"
                "Expected one of:\n"
                "  - a 2-element list/tuple [input_price, output_price], or\n"
                "  - a dict with at least 'input' and 'output' keys "
                "(plus optional 'cached_input', 'cached_write_5m', "
                "'cached_write_1h', 'reasoning', 'batch_discount').\n"
                "See pricing.json in the repo root for working examples."
            )
    return out


def _apply_embeddings_semantic(
    report: dict[str, Any],
    *,
    baseline: list[dict[str, Any]],
    candidate: list[dict[str, Any]],
    seed: int,
) -> dict[str, Any]:
    """Swap axis 1 (semantic) with a real sentence-transformer result."""
    try:
        from shadow.embeddings import SemanticEmbedder, recompute_semantic_axis
    except ImportError:
        err_console.print(
            "[yellow]warning[/]: --semantic embeddings requested but "
            "`shadow[embeddings]` isn't installed; falling back to BM25."
        )
        return report
    try:
        embedder = SemanticEmbedder()
    except ShadowError as e:
        err_console.print(f"[yellow]warning[/]: {e}; falling back to BM25.")
        return report
    new_row = recompute_semantic_axis(baseline, candidate, embedder, seed=seed)
    new_rows = [new_row if r.get("axis") == "semantic" else r for r in report.get("rows", [])]
    report = {**report, "rows": new_rows}
    return _refresh_after_axis_swap(report, "semantic", new_row)


def _refresh_after_axis_swap(
    report: dict[str, Any], axis: str, new_row: dict[str, Any]
) -> dict[str, Any]:
    """Drop stale recommendations / drill-down entries that referred to
    ``axis`` before the swap, and add a fresh aggregate recommendation
    if the new row's severity warrants one.

    Without this, an embeddings-backed semantic recompute that *lowered*
    severity from severe to minor would leave the original
    "Review semantic axis: severe" recommendation untouched in the
    report — directly contradicting the new row. Same for any per-pair
    drill-down score on the swapped axis, which is still based on the
    pre-swap (BM25-on-semantic, sanity-on-judge) computation.
    """
    rank = {"none": 0, "minor": 1, "moderate": 2, "severe": 3}
    new_severity = str(new_row.get("severity", "none"))

    # 1) Drop recommendations whose `axis` field matches the swapped axis.
    recs = [r for r in (report.get("recommendations") or []) if r.get("axis") != axis]

    # 2) If the swapped axis is still moderate or worse, add a fresh
    #    aggregate-level recommendation reflecting the post-swap numbers.
    if rank.get(new_severity, 0) >= rank["moderate"]:
        delta = float(new_row.get("delta", 0.0))
        ci_low = float(new_row.get("ci95_low", 0.0))
        ci_high = float(new_row.get("ci95_high", 0.0))
        sev = "error" if new_severity == "severe" else "warning"
        recs.insert(
            0,
            {
                "severity": sev,
                "action": "review",
                "turn": 0,
                "message": (
                    f"Review the candidate: {axis} axis shifted " f"with severity {new_severity}."
                ),
                "rationale": (
                    f"Aggregate signal recomputed after axis swap "
                    f"({axis}: delta {delta:+.3f}, CI [{ci_low:+.3f}, {ci_high:+.3f}])."
                ),
                "axis": axis,
                "confidence": 0.8,
            },
        )

    # 3) Strip the swapped-axis entry from every drill-down pair's
    #    axis_scores. Recompute dominant_axis + regression_score from the
    #    remaining entries so per-pair output is consistent with the new
    #    aggregate row. Pairs that had the swapped axis as dominant get a
    #    new dominant axis from whatever's left.
    drill_down = report.get("drill_down") or []
    new_drill: list[dict[str, Any]] = []
    for pair in drill_down:
        scores = [s for s in (pair.get("axis_scores") or []) if s.get("axis") != axis]
        if not scores:
            continue
        # regression_score = sum of normalized_delta across remaining axes
        regression_score = sum(float(s.get("normalized_delta", 0.0)) for s in scores)
        # dominant_axis = highest absolute normalized_delta
        dominant = max(
            scores,
            key=lambda s: abs(float(s.get("normalized_delta", 0.0))),
            default=None,
        )
        new_drill.append(
            {
                **pair,
                "axis_scores": scores,
                "regression_score": regression_score,
                "dominant_axis": dominant.get("axis") if dominant else "",
            }
        )

    # 4) If the first divergence's dominant axis was the swapped one,
    #    drop it — we don't have a clean way to re-pick without a full
    #    pair-level recompute.
    first_div = report.get("first_divergence")
    if isinstance(first_div, dict) and first_div.get("axis") == axis:
        first_div = None

    return {
        **report,
        "recommendations": recs,
        "drill_down": new_drill,
        "first_divergence": first_div,
    }


def _apply_judge(
    report: dict[str, Any],
    *,
    baseline_records: list[dict[str, Any]],
    candidate_records: list[dict[str, Any]],
    judge_kind: JudgeKind,
    judge_backend: JudgeBackend,
    judge_model: str | None,
    judge_config: Path | None,
    seed: int,
) -> dict[str, Any]:
    from shadow.judge import aggregate_scores
    from shadow.llm import get_backend

    pairs = _pair_responses(baseline_records, candidate_records)
    if not pairs:
        return report

    backend = get_backend(judge_backend.value)
    cfg = _load_judge_config(judge_config) if judge_config is not None else {}
    judge_obj = _build_judge(judge_kind, backend, judge_model, cfg)

    async def _run() -> list[float]:
        out: list[float] = []
        for b_resp, c_resp, req_ctx in pairs:
            verdict = await judge_obj.score_pair(b_resp, c_resp, req_ctx)
            out.append(verdict["score"])
        return out

    scores = asyncio.run(_run())
    new_row = aggregate_scores(scores, seed=seed)
    new_rows = [new_row if r.get("axis") == "judge" else r for r in report.get("rows", [])]
    report = {**report, "rows": new_rows}
    return _refresh_after_axis_swap(report, "judge", new_row)


def _load_judge_config(path: Path) -> dict[str, Any]:
    """Parse the --judge-config YAML file."""
    raw = yaml.safe_load(path.read_text())
    if not isinstance(raw, dict):
        raise ShadowError(
            f"--judge-config {path} must be a YAML mapping (got {type(raw).__name__})"
        )
    return raw


def _build_judge(
    kind: JudgeKind,
    backend: Any,
    model: str | None,
    cfg: dict[str, Any],
) -> Any:
    """Construct a Judge instance based on `--judge <kind>` + config data."""
    from shadow.judge import (
        FactualityJudge,
        LlmJudge,
        PairwiseJudge,
        ProcedureAdherenceJudge,
        RefusalAppropriateJudge,
        SanityJudge,
        SchemaConformanceJudge,
        ToneJudge,
    )

    def _require(key: str) -> Any:
        if key not in cfg:
            raise ShadowError(f"--judge {kind.value} requires '{key}' in --judge-config")
        return cfg[key]

    if kind is JudgeKind.sanity:
        return SanityJudge(backend, model=model)
    if kind is JudgeKind.pairwise:
        return PairwiseJudge(backend, model=model)
    if kind is JudgeKind.llm:
        return LlmJudge(
            backend,
            rubric=_require("rubric"),
            score_map=cfg.get("score_map"),
            model=model,
        )
    if kind is JudgeKind.procedure:
        return ProcedureAdherenceJudge(
            backend, required_procedure=_require("required_procedure"), model=model
        )
    if kind is JudgeKind.schema:
        return SchemaConformanceJudge(
            backend, expected_schema=_require("expected_schema"), model=model
        )
    if kind is JudgeKind.factuality:
        return FactualityJudge(backend, known_facts=_require("known_facts"), model=model)
    if kind is JudgeKind.refusal:
        return RefusalAppropriateJudge(backend, policy=_require("policy"), model=model)
    if kind is JudgeKind.tone:
        return ToneJudge(backend, target_tone=_require("target_tone"), model=model)
    raise ShadowError(f"unsupported --judge kind: {kind}")


def _pair_responses(
    baseline_records: list[dict[str, Any]],
    candidate_records: list[dict[str, Any]],
) -> list[tuple[dict[str, Any], dict[str, Any], dict[str, Any] | None]]:
    """Pair the i-th chat_response from baseline with the i-th from candidate.

    Each tuple also carries the baseline chat_request that preceded it (if
    any) so the judge has task context.
    """
    b_resps: list[dict[str, Any]] = []
    b_reqs_before: list[dict[str, Any] | None] = []
    last_req: dict[str, Any] | None = None
    for r in baseline_records:
        kind = r.get("kind")
        if kind == "chat_request":
            last_req = r.get("payload")
        elif kind == "chat_response":
            b_resps.append(r.get("payload") or {})
            b_reqs_before.append(last_req)
    c_resps: list[dict[str, Any]] = [
        r.get("payload") or {} for r in candidate_records if r.get("kind") == "chat_response"
    ]
    paired: list[tuple[dict[str, Any], dict[str, Any], dict[str, Any] | None]] = []
    for i in range(min(len(b_resps), len(c_resps))):
        paired.append((b_resps[i], c_resps[i], b_reqs_before[i]))
    return paired


