@app.command("mine", rich_help_panel="Replay & analysis")
def mine_cmd(
    traces: Annotated[
        list[Path],
        typer.Argument(
            help="One or more .agentlog files to mine for representative cases.",
        ),
    ],
    output: Annotated[
        Path, typer.Option("--output", "-o", help="Where to write the mined .agentlog")
    ],
    max_cases: Annotated[
        int,
        typer.Option(
            "--max-cases",
            help="Cap on total selected pairs (default 50).",
        ),
    ] = 50,
    per_cluster: Annotated[
        int,
        typer.Option(
            "--per-cluster",
            help="Cap on how many pairs to keep from each cluster (default 1).",
        ),
    ] = 1,
    pricing: Annotated[
        Path | None,
        typer.Option("--pricing", help="Optional pricing.json for cost-aware scoring."),
    ] = None,
) -> None:
    """Pick representative turn-pairs out of a production trace set.

    Clusters all chat_request/chat_response pairs by tool-sequence +
    stop_reason + verbosity and latency buckets, then picks the most
    interesting example from each cluster (errors, refusals, high
    cost, heavy reasoning, very short/long responses). Writes a new
    .agentlog containing just the selected turns.

    The result is a compact, representative fixture set suitable for
    running `shadow diff` in CI against every PR.

    Each positional ``traces`` argument can be either a single
    ``.agentlog`` file or a directory; directories are recursively
    walked and every ``*.agentlog`` file inside is mined. This makes
    pointing the command at a production trace dump (the typical
    use case) a one-liner instead of a shell-glob dance.
    """
    from shadow.mine import mine as do_mine

    # Expand each input into the concrete list of .agentlog files to
    # mine. A directory expands to every `*.agentlog` underneath it
    # (recursively); a file passes through unchanged. Missing paths
    # and directories without any traces raise typed errors so the
    # CLI surfaces a hint instead of leaking IsADirectoryError /
    # FileNotFoundError tracebacks. Same UX as `shadow holdout` does
    # for its `--base` argument when a directory is passed.
    expanded: list[Path] = []
    for path in traces:
        if not path.exists():
            _fail(
                ShadowError(
                    f"`{path}` does not exist; pass an existing .agentlog "
                    f"file or a directory containing them."
                )
            )
        if path.is_dir():
            matched = sorted(path.rglob("*.agentlog"))
            if not matched:
                _fail(
                    ShadowError(
                        f"`{path}` is a directory but contains no `*.agentlog` files; "
                        f"point at a trace dump or pass individual files."
                    )
                )
            expanded.extend(matched)
        else:
            expanded.append(path)

    try:
        parsed_traces = []
        for path in expanded:
            parsed_traces.append(_core.parse_agentlog(path.read_bytes()))
        price_map: dict[str, Any] | None = None
        if pricing is not None:
            price_map = load_pricing_file(pricing)
        result = do_mine(
            parsed_traces,
            max_cases=max_cases,
            per_cluster=per_cluster,
            pricing=price_map,
        )
        output.parent.mkdir(parents=True, exist_ok=True)
        output.write_bytes(_core.write_agentlog(result.to_agentlog()))
        err_console.print(
            f"[green]✓[/] mined {len(result.cases)} cases from "
            f"{result.total_input_pairs} pairs in "
            f"{result.clusters_found} clusters → {output}"
        )
    except ShadowError as e:
        _fail(e)
    except Exception as e:
        _fail(e)


