@app.command(
    # Without these, Typer 0.24 treats the `--` separator as "end of
    # options" but still errors out on unrecognised positional args
    # after it. Explicit per-command allow_extra_args forwards the
    # tokens after `--` to ctx.args as raw strings.
    context_settings={"allow_extra_args": True, "ignore_unknown_options": True},
    rich_help_panel="Common",
)
def record(
    ctx: typer.Context,
    output: Annotated[
        Path, typer.Option("--output", "-o", help="Where to write the .agentlog file")
    ] = Path("recording.agentlog"),
    tags: Annotated[
        str | None,
        typer.Option(
            "--tags",
            help="Comma-separated key=value tags for the trace metadata "
            "(e.g. --tags env=dev,branch=main).",
        ),
    ] = None,
    no_auto_instrument: Annotated[
        bool,
        typer.Option(
            "--no-auto-instrument",
            help="Disable zero-config instrumentation. Use this when the "
            "wrapped command already opens its own `Session` and you don't "
            "want a second one wrapping it.",
        ),
    ] = False,
) -> None:
    """Run a subcommand with zero-config auto-instrumentation.

    Example:
        shadow record -o demo.agentlog -- python agent.py

    The wrapped command's Python interpreter loads a sitecustomize
    shim that starts a `Session` around the whole process. Every
    `anthropic.*.messages.create` / `openai.*.chat.completions.create`
    call made by the agent gets recorded without the agent touching
    any Shadow code.

    If the wrapped command already imports `shadow.sdk.Session` on
    its own, pass `--no-auto-instrument` to avoid a double session.
    """
    args = ctx.args
    if not args:
        err_console.print("[red]error[/]: specify a command after `--`")
        raise typer.Exit(code=2)

    # Fail fast if the output path isn't writable. Catching this here —
    # before the agent runs — prevents wasting LLM API calls on a
    # recording that can't be saved.
    try:
        resolved = output.resolve()
        resolved.parent.mkdir(parents=True, exist_ok=True)
        # Probe actual writability (mkdir tolerates read-only parents
        # silently on some FSes; an open-for-append round-trips the real
        # permission check and leaves no partial bytes behind).
        with resolved.open("ab"):
            pass
        if resolved.stat().st_size == 0:
            # We just created the file via append; clean up so an empty
            # .agentlog doesn't mislead `shadow diff` later.
            resolved.unlink()
    except OSError as e:
        err_console.print(
            f"[red]error[/]: output path not writable: {output}\n"
            f"  reason: {e}\n"
            f"  hint: check the directory exists and you have write permission, "
            "then re-run."
        )
        raise typer.Exit(code=2) from None

    env = dict(os.environ)
    env["SHADOW_SESSION_OUTPUT"] = str(resolved)
    if tags:
        env["SHADOW_SESSION_TAGS"] = tags
    if not no_auto_instrument:
        # Prepend the bootstrap dir so our sitecustomize wins over any
        # other sitecustomize on the user's PYTHONPATH. Python's site.py
        # imports the first `sitecustomize` it finds on sys.path, so
        # prepending is the deterministic win.
        from shadow.sdk import _bootstrap

        bootstrap_dir = str(Path(_bootstrap.__file__).resolve().parent)
        existing = env.get("PYTHONPATH", "")
        env["PYTHONPATH"] = f"{bootstrap_dir}{os.pathsep}{existing}" if existing else bootstrap_dir
    result = subprocess.run(args, env=env, check=False)
    raise typer.Exit(code=result.returncode)


