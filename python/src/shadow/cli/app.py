def _version_callback(value: bool) -> None:
    """Print version and exit. Wired to ``shadow --version`` so the
    standard CLI convention works alongside the existing
    ``shadow version`` subcommand."""
    if not value:
        return
    from shadow import SPEC_VERSION, __version__

    typer.echo(f"shadow {__version__} (spec {SPEC_VERSION})")
    raise typer.Exit()


