@app.command(rich_help_panel="Setup")
def quickstart(
    path: Annotated[
        Path,
        typer.Argument(help="Directory to scaffold into (created if missing)"),
    ] = Path("shadow-quickstart"),
    force: Annotated[
        bool,
        typer.Option(
            "--force",
            help="Overwrite files in PATH even if they already exist.",
        ),
    ] = False,
) -> None:
    """Scaffold a minimal runnable Shadow scenario in PATH.

    Drops a toy agent, two configs (differing on three known axes),
    and pre-recorded baseline + candidate `.agentlog` fixtures. A
    brand-new user can go from `pip install shadow-diff` to seeing
    a real nine-axis diff in under a minute, no API keys required.

    Example:
        shadow quickstart              # scaffolds into ./shadow-quickstart/
        shadow quickstart my-demo/     # scaffolds into ./my-demo/
        shadow quickstart --force      # overwrites existing files
    """
    from importlib import resources

    try:
        import shadow.quickstart_data as _qs_data
    except ImportError as e:  # pragma: no cover — wheel always ships this
        _fail(Exception(f"shadow[quickstart-data] missing from install: {e}"))
        return

    dest = path.resolve()
    # Path-traversal hardening: refuse to scaffold into a system
    # directory. Users should be able to `shadow quickstart ~/projects/foo/`
    # but not accidentally wipe `/etc/` via shell-expansion mishaps.
    forbidden_prefixes = ("/etc", "/usr", "/bin", "/sbin", "/boot", "/proc", "/sys", "/dev")
    if any(str(dest).startswith(p + "/") or str(dest) == p for p in forbidden_prefixes):
        _fail(Exception(f"refusing to scaffold into a system directory: {dest}"))
        return
    dest.mkdir(parents=True, exist_ok=True)
    written: list[Path] = []
    skipped: list[Path] = []

    # Files to copy from the packaged quickstart_data resource. Two-level
    # list because `fixtures/` is a subdirectory. Keeps the copy logic
    # trivial — no need to recursively walk the package.
    plan: list[tuple[str, Path]] = [
        ("agent.py", dest / "agent.py"),
        ("config_a.yaml", dest / "config_a.yaml"),
        ("config_b.yaml", dest / "config_b.yaml"),
        ("README.md", dest / "QUICKSTART.md"),
    ]
    fixtures_dest = dest / "fixtures"
    fixtures_dest.mkdir(exist_ok=True)
    fixture_plan: list[tuple[str, Path]] = [
        ("baseline.agentlog", fixtures_dest / "baseline.agentlog"),
        ("candidate.agentlog", fixtures_dest / "candidate.agentlog"),
    ]

    root = resources.files(_qs_data)
    try:
        for resource_name, target in plan:
            if target.exists() and not force:
                skipped.append(target)
                continue
            target.write_bytes(root.joinpath(resource_name).read_bytes())
            written.append(target)
        for resource_name, target in fixture_plan:
            if target.exists() and not force:
                skipped.append(target)
                continue
            target.write_bytes(root.joinpath("fixtures").joinpath(resource_name).read_bytes())
            written.append(target)
    except Exception as e:
        _fail(e)
        return

    def _rel(p: Path) -> Path:
        return p.relative_to(Path.cwd()) if p.is_relative_to(Path.cwd()) else p

    for p in written:
        err_console.print(f"[green]✓[/] wrote {_rel(p)}")
    for p in skipped:
        err_console.print(
            f"[yellow]·[/] skipped {_rel(p)} (already exists; use --force to overwrite)"
        )

    if written:
        rel = _rel(dest)
        err_console.print("")
        err_console.print(f"[bold]Next steps[/] ({rel}/):")
        err_console.print(f"  1.  cd {rel}")
        err_console.print(
            "  2.  shadow diff fixtures/baseline.agentlog " "fixtures/candidate.agentlog"
        )
        err_console.print("")
        err_console.print("[dim]See QUICKSTART.md for how to record your own agent's traces.[/]")


