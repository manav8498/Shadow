@app.command("heal", rich_help_panel="Replay & analysis")
def heal_cmd(
    baseline: Annotated[
        Path, typer.Argument(help="Anchor .agentlog file (the known-good reference)")
    ],
    candidate: Annotated[
        Path, typer.Argument(help="Candidate .agentlog file (the trace under review)")
    ],
    seed: Annotated[int, typer.Option("--seed", help="RNG seed for the bootstrap")] = 42,
    json_output: Annotated[
        bool,
        typer.Option(
            "--json",
            help="Emit the decision as JSON to stdout instead of the rendered panel.",
        ),
    ] = False,
    log: Annotated[
        bool,
        typer.Option(
            "--log",
            help="Append the heal decision to the local artifact ledger so "
            "`shadow ledger` and `shadow trail` see it later. Default: off.",
        ),
    ] = False,
) -> None:
    """Classify a regression into one of three heal tiers.

    Runs the audit-only causal classifier against two `.agentlog`
    traces and prints one of three tiers:

      * `heal`    — every gate passed; the change is provably cosmetic
                    or below the noise floor.
      * `propose` — gates passed but non-cosmetic divergences remain;
                    a future auto-mode would save this as a candidate
                    variant for the user to approve.
      * `hold`    — at least one gate refused. No automatic action is
                    safe; surface to a reviewer.

    This phase ships *classification only* — no `--apply` flag, no
    automatic action. The output is the decision plus the full list
    of evidence each gate produced. Users act on the recommendation
    manually; the classifier earns trust before automation is enabled.

    Example:
        shadow heal anchor.agentlog candidate.agentlog
        shadow heal anchor.agentlog candidate.agentlog --json | jq .tier
        shadow heal anchor.agentlog candidate.agentlog --log

    Related:
        shadow call    decide ship/hold/probe/stop on the same pair
        shadow autopr  pin the regression as a policy when not healable
        shadow trail   walk back through the artifact graph
    """
    import json as _json

    from shadow.heal import classify, render_decision

    try:
        b = _core.parse_agentlog(baseline.read_bytes())
        c = _core.parse_agentlog(candidate.read_bytes())
        report = _core.compute_diff_report(b, c, None, seed)
    except (FileNotFoundError, PermissionError) as e:
        _fail(e)
        return
    except ShadowError as e:
        _fail(e)
        return
    except Exception as e:  # pragma: no cover — defensive: parser surfaces ShadowError.
        _fail(e)
        return

    decision = classify(report)

    if json_output:
        sys.stdout.write(_json.dumps(decision.to_dict(), indent=2) + "\n")
    else:
        render_decision(decision, console=console)

    if log:
        # Reuse the existing ledger by writing a `kind: heal` entry.
        # The schema's ``kind`` field accepts free-form strings; the
        # store's CRUD layer doesn't care which value lands.
        from shadow.ledger import LedgerEntry, write_entry

        try:
            entry = LedgerEntry(
                kind="heal",
                timestamp=_now_iso_for_heal(),
                anchor_id=decision.anchor_id,
                candidate_id=decision.candidate_id,
                tier=decision.tier.value,
                pair_count=decision.pair_count,
                driver_summary=decision.rationale[:200] if decision.rationale else None,
                source_command="shadow heal",
            )
            path = write_entry(entry)
            err_console.print(f"[dim]✓ logged heal decision to {path}[/]")
        except OSError as e:
            err_console.print(f"[yellow]warning[/]: ledger write failed: {e}")


def _now_iso_for_heal() -> str:
    """ISO-8601 UTC timestamp with microsecond precision.

    Matches the format the ledger store uses internally so that
    ``shadow ledger`` / ``shadow trail`` see heal entries on the same
    timeline as call / diff entries.
    """
    from datetime import UTC, datetime

    return datetime.now(UTC).strftime("%Y-%m-%dT%H:%M:%S.%f") + "Z"


# ---- holdout ---------------------------------------------------------------

holdout_app = typer.Typer(
    name="holdout",
    help="Manage held-out trace ids (acknowledged-but-not-blocking).",
    no_args_is_help=True,
    add_completion=False,
)


@holdout_app.command("add")
def holdout_add_cmd(
    trace_id: Annotated[str, typer.Argument(help="Trace id (8-char prefix is enough).")],
    reason: Annotated[
        str,
        typer.Option(
            "--reason",
            help="Why this trace is held out. Surface it next to the entry "
            "in `shadow holdout list` so the team remembers the triage.",
        ),
    ],
    owner: Annotated[
        str,
        typer.Option(
            "--owner",
            help="Who is responsible for the holdout. Free-form (e.g. `@alice`).",
        ),
    ],
    ttl: Annotated[
        str,
        typer.Option(
            "--ttl",
            help="How long before the holdout goes stale and demands review. "
            "Default `30d`. Sub-day units round up to one day.",
        ),
    ] = "30d",
    base: Annotated[
        Path | None,
        typer.Option(
            "--base",
            help="Override the holdout file path. Default `.shadow/holdout.json`.",
        ),
    ] = None,
) -> None:
    """Add a trace id to the holdout set, or refresh an existing one."""
    from datetime import UTC
    from datetime import datetime as _datetime

    from shadow.holdout import add_entry, load, parse_ttl, save

    try:
        ttl_days = parse_ttl(ttl)
    except ValueError as e:
        _fail(e)
        return

    holdouts = load(path=base)
    holdouts = add_entry(
        holdouts,
        trace_id=trace_id,
        reason=reason,
        owner=owner,
        ttl_days=ttl_days,
        now=_datetime.now(UTC),
    )
    try:
        out = save(holdouts, path=base)
    except ValueError as e:
        _fail(e)
        return
    err_console.print(f"[green]✓[/] held out [cyan]{trace_id}[/] (file: {out})")


@holdout_app.command("remove")
def holdout_remove_cmd(
    trace_id: Annotated[str, typer.Argument(help="Trace id to drop from the holdout set.")],
    base: Annotated[
        Path | None,
        typer.Option("--base", help="Override the holdout file path."),
    ] = None,
) -> None:
    """Drop a trace id from the holdout set."""
    from shadow.holdout import load, remove_entry, save

    holdouts = load(path=base)
    holdouts, found = remove_entry(holdouts, trace_id)
    if not found:
        err_console.print(f"[yellow]warning[/]: {trace_id!r} is not in the holdout set.")
        return
    try:
        save(holdouts, path=base)
    except ValueError as e:
        _fail(e)
        return
    err_console.print(f"[green]✓[/] removed [cyan]{trace_id}[/]")


@holdout_app.command("reset")
def holdout_reset_cmd(
    trace_id: Annotated[str, typer.Argument(help="Trace id whose review window to restart.")],
    base: Annotated[
        Path | None,
        typer.Option("--base", help="Override the holdout file path."),
    ] = None,
) -> None:
    """Restart the review window for an existing holdout entry."""
    from datetime import UTC
    from datetime import datetime as _datetime

    from shadow.holdout import load, reset_entry, save

    holdouts = load(path=base)
    holdouts, found = reset_entry(holdouts, trace_id, now=_datetime.now(UTC))
    if not found:
        err_console.print(f"[yellow]warning[/]: {trace_id!r} is not in the holdout set.")
        return
    try:
        save(holdouts, path=base)
    except ValueError as e:
        _fail(e)
        return
    err_console.print(f"[green]✓[/] reset review window for [cyan]{trace_id}[/]")


@holdout_app.command("list")
def holdout_list_cmd(
    stale: Annotated[
        bool,
        typer.Option(
            "--stale",
            help="Only show entries past their review window.",
        ),
    ] = False,
    json_output: Annotated[
        bool,
        typer.Option(
            "--json",
            help="Emit the holdout set as JSON instead of the rendered panel.",
        ),
    ] = False,
    base: Annotated[
        Path | None,
        typer.Option("--base", help="Override the holdout file path."),
    ] = None,
) -> None:
    """List held-out trace ids."""
    import json as _json
    from datetime import UTC
    from datetime import datetime as _datetime

    from shadow.holdout import Holdouts, load, render

    now = _datetime.now(UTC)
    holdouts = load(path=base)

    if stale:
        filtered = {tid: e for tid, e in holdouts.entries.items() if e.is_stale(now=now)}
        holdouts = Holdouts(entries=filtered, schema_version=holdouts.schema_version)

    if json_output:
        sys.stdout.write(_json.dumps(holdouts.to_dict(), indent=2) + "\n")
        return

    render(holdouts, now=now, console=console)


app.add_typer(holdout_app, rich_help_panel="Reporting & history")


